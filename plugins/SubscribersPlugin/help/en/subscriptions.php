<p>
This page displays a summary by month of subscriptions and unsubscriptions.
</p>
<p>The chart shows subscriptions by month for the periods in the result table. Each bar has up to three
sections: active, blacklisted and unconfirmed. Together these add-up to the number of subscriptions.</p>
<p>A line showing the number of unsubscriptions per month is overlaid on the bar chart.
</p>
<table>
<tr>
<td>Period</td>
<td>The year and month.</td>
</tr>
<tr>
<td>Subscriptions</td>
<td>The number of new subscribers during the month. This total is calculated using the 'entered' date in the user table.
The value will be the sum of the next three columns: active, blacklisted and unconfirmed.</td>
</tr>
<tr>
<td>Active</td>
<td>The number of subscribers who are now confirmed and not blacklisted.</td>
</tr>
<tr>
<td>Blacklisted</td>
<td>The number of subscribers who are now confirmed and blacklisted. 
The subscribers did not necessarily change to blacklisted in this month.</td>
</tr>
<tr>
<td>Unconfirmed</td>
<td>The number of subscribers who are unconfirmed.</td>
</tr>
<tr>
<td>Unsubscriptions</td>
<td>The number of unsubscriptions. 
This value is different to the Blacklisted total as it reflects when the unsubscription occured rather than when the subscription occured.
</td>
</tr>
<tr>
<td>Total</td>
<td>Sum of the values for each month displayed for each column.</td>
</tr>
</table>
